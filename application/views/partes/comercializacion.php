<h3>TU TIENDA DE MASCOTAS ONLINE</h3>
<p>Ahorrá tiempo y dinero haciendo tu compra en Animalia. Contamos con el más amplio stock de productos, las mejores ofertas, y orderná sin dejar la comodidad de tu casa.</p><br><br>

 

<h4>NECESITAS AYUDA PARA COMPRAR?</h4>
<p>1.Elige el producto que deseas comprar. Utiliza el menú superior para ver las distintas categorías.</p>
<p>2.Haz clic en el botón de "Agregar al carrito". Esto agregará el producto a tu carrito y te llevará al mismo.</p>
<p>3.Puedes seguir agregando otros productos al carrito o sino haz clic en "Iniciar Compra".</p>
<p>4.Completa tus datos de contacto. Si ya tienes una cuenta, puedes iniciar sesión con la misma.</p>
<p>5.Selecciona si deseas que te enviemos el pedido o si lo retiras personalmente en alguna de nuestras sucursales. En ambas opciones debes completar los datos correspondientes y hacer clic en “Continuar”.</p>
<p>6.Selecciona el método de envío que desees:
Los envíos en CABA son sin cargo y los realizamos nosotros mismos.</p>
<p>7.Los envíos en GBA y resto del país los realizamos a través de OCA.</p>
<p>8.Elige el medio de pago.</p>
<p>9.Puedes seleccionar la opción de "Efectivo en domicilio" (exclusivo en CABA) y obtener un descuento extra, o abonar con tarjeta de crédito o debito.</p>
<p>10.Una vez que hayas elegido el medio de pago, haz clic en "Pagar a través de…"</p>
<p>11.Finalmente, en la página de Confirmación de compra puedes revisar toda la información de la compra realizada.</p>
<p>12.Recibirás un correo de nuestra parte, ese no será un comprobante de pago.</p>
<p>13.Por último, nos pondremos en contacto telefónicamente o por Whatsapp para coordinar detalles de la entrega o retiro. Te recordamos que los horarios de contacto son de Lunes a Viernes de 9 a 20 Hs y los Sábados de 9 a 14 Hs. Las compras realizadas fuera de esas franjas horarias serán contactadas en el siguiente horario disponible.</p>
